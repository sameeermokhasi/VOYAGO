export const getAIResponse = (input, role = 'rider') => {
    const lowerInput = input.toLowerCase();

    // 1. Direct Keyword Matching
    for (const [key, response] of Object.entries(chatbotData)) {
        if (lowerInput.includes(key)) {
            // Role-based filtering for very specific generic keywords could go here
            // But usually, general info is fine for everyone.
            // Exception: Driver specific keywords
            if (response.includes("(Driver)") && role !== 'driver') {
                continue; // Skip driver message if user is rider
            }
            return response;
        }
    }

    // 2. Default Fallbacks
    if (role === 'driver') {
        return "I'm here to help partners! Ask about earnings, incentives, or ratings.";
    }

    return "I'm currently trained on 100+ travel topics! Try asking about booking, payments, safety, or vacations. If I can't help, contact support@voyago.com.";
};
